#include "libft.h"

char	*ft_strdup(char *s)
{
	char	*ptr;
	int		i;

	ptr = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	i = 0;
	if (!ptr)
		return (0);
	while (s[i] != '\0')
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
